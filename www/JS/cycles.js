function whileExample() {
    let count = 0;
    while (count < 10 ){
        console.log(count);
        count++;
    }
}

function doWhileExample(){
    let count = 10;
    do {
        console.log(count);
        count++;
    } while (count < 10);
}

function forExample(){
    for(let i = 0; i < 6; i++){
        alert(i);
    }
}